%% init_multi.m - 多机编队参数初始化 (融合 IAPF + Wang 动力学)
clear; clc;

% =========================================================================
% 1. 物理参数 (所有无人机相同)
% =========================================================================
num_agents = 4; % 无人机数量 (跟随者数量，Node 1~4)
m = 1; 
g = 9.81; 
J = diag([0.04856, 0.04856, 0.08801]); 
e3 = [0; 0; 1];

% =========================================================================
% 2. 通信拓扑定义 (快捷修改区)
% =========================================================================
% [快捷修改] 定义领航者 (Node 0) 连向谁？
% 例如: [1] 表示 0->1; [1, 4] 表示 0->1 和 0->4
leader_connections = [1]; 

% [快捷修改] 定义 Follower 之间的连接 (双向/无向)
follower_edges = [ ...
    1, 2;
    2, 3;
    2, 4 ...
    ];

% === 自动生成矩阵 (无需修改) ===
d0 = zeros(num_agents, 1);
d0(leader_connections) = 1; 

D = zeros(num_agents, num_agents);
for k = 1:size(follower_edges, 1)
    i = follower_edges(k, 1);
    j = follower_edges(k, 2);
    D(i, j) = 1;
    D(j, i) = 1; % 自动保证对称性 (无向图)
end

L = zeros(num_agents, num_agents);
for i = 1:num_agents
    L(i,i) = sum(D(i,:)); 
    for j = 1:num_agents
        if D(i,j) == 1
            L(i,j) = -1;
        end
    end
end
M = L + diag(d0);

% =========================================================================
% 3. 期望编队偏差 delta 
% =========================================================================
% delta 是 3x4 矩阵，每一列代表一架飞机的偏差 [dx; dy; dz]
delta = zeros(3, 4);
delta(:,1) = [ 10;  10; 0];
delta(:,2) = [-10;  10; 0];
delta(:,3) = [-10; -10; 0];
delta(:,4) = [ 10; -10; 0];

% =========================================================================
% 4. 初始状态
% =========================================================================
% 领航者 (Node 0) 的初始物理状态
p0_leader = [0; 0; 0];
v0_leader = [0; 0; 0];
Q0_leader = [1; 0; 0; 0];
w0_leader = [0; 0; 0];


% p0_all = [ 5,  9,  4, -1; 
%            3, -4, -2,  4; 
%           -1,  1, -3, -2]; 
p0_all = p0_leader + delta;
v0_all = zeros(3, 4); 
w0_all = zeros(3, 4); 
Q0_all = repmat([1; 0; 0; 0], 1, 4); 

% 合并生成 13 x 5 的总状态矩阵 (传给积分器的 xInitial)
xInitial = [ [p0_leader; v0_leader; Q0_leader; w0_leader], ...
             [p0_all; v0_all; Q0_all; w0_all] ];

% =========================================================================
% 5. 估计器初始状态
% =========================================================================
rho0 = zeros(3, 4);
a_hat0 = zeros(3, 4);

% =========================================================================
% 6. 一致性与姿态控制参数 (Wang)
% =========================================================================
kp = 5;      % 位置一致性增益
kv = 15;      % 速度一致性增益
k_rho = 12; % 估计器参数
ka = 5;     % 估计器参数 (注意：这里是估计器的增益)
kq = 8;     % 姿态环参数 (四元数误差增益)
kt = 20;     % 姿态环参数 (不需要了的话后续可删，保留兼容原代码)
kw = 4;      % 姿态环参数 (角速度误差增益)

alpha = 0.5; % 有限时间收敛参数

% =========================================================================
% 7. 宏观导航与避障参数 (新增：Pan的IAPF融合部分)
% =========================================================================
q_goal = [50; 50; 50];         % 全局目标点 (Goal)

% 障碍物属性
obs_center = [ 20,  30,  10;    % X 坐标
               10,  30,  50;    % Y 坐标
               20,  25,  25 ];  % Z 坐标     % 障碍物中心位置 (放置在起点与终点之间)
           
obs_radius = [ 4,  4,  4];      % 障碍物实体半径
obs_rho0   = [15, 15, 15];      % 障碍物警戒半径 (进入此范围触发斥力)

% IAPF 核心增益系数
k_att  = 0.5;   % 目标点对领航者的引力增益 (原计划叫ka，为防冲突改名)
eta    = 20;   % 障碍物斥力增益 (对应原公式中的 k_r)
eta_follower = 10;
k_d    = 5;     % 速度阻尼项增益 (充当刹车，防止越过目标点和震荡)
n_goal = 2;     % Ge & Cui 斥力公式中的目标距离距离指数
k_e    = 5;    % 局部极小值旋转力增益

% =========================================================================
% 8. 仿真设置
% =========================================================================
sim_time = 80;
dt = 0.001;

fprintf('参数初始化完成！\n已生成 %d 个跟随者智能体与 1 个领航者的拓扑结构。\n', num_agents);